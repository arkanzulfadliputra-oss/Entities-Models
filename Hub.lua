local AngryFace = "rbxassetid://79076579351847"

local NormalSoundId = "rbxassetid://88526000225516"
local AngrySoundId = "rbxassetid://84909470598244"

ImageLabel.Image = NormalFace

local Sound = Instance.new("Sound")
Sound.Parent = ScreenGui
Sound.Volume = 5

local HasTriggered = false
local LastMoveTime = tick()

Sound.SoundId = NormalSoundId
Sound:Play()

task.spawn(function()
	while ScreenGui.Parent do
		if not HasTriggered then
			if tick() - LastMoveTime >= 5 then
				HasTriggered = true

				local FadeTween = TweenService:Create(
					ImageLabel,
					TweenInfo.new(1),
					{ImageTransparency = 1}
				)
				FadeTween:Play()

				local RedFade = TweenService:Create(
					RedFrame,
					TweenInfo.new(1),
					{BackgroundTransparency = 1}
				)
				RedFade:Play()

				FadeTween.Completed:Connect(function()
					Sound:Destroy()
					ScreenGui:Destroy()
				end)
			end
		end
		task.wait(0.2)
	end
end)

RunService.RenderStepped:Connect(function()
	if HasTriggered then return end

	if Humanoid.MoveDirection.Magnitude > 0 then
		LastMoveTime = tick()

		HasTriggered = true

		ImageLabel.Image = AngryFace

		Sound:Stop()
		Sound.SoundId = AngrySoundId
		Sound:Play()


		local Flash = TweenService:Create(
			RedFrame,
			TweenInfo.new(0.15),
			{BackgroundTransparency = 0.2}
		)
		Flash:Play()

		task.wait(1)

		local FadeTween = TweenService:Create(
			ImageLabel,
			TweenInfo.new(1),
			{ImageTransparency = 1}
		)
		FadeTween:Play()

		local RedFade = TweenService:Create(
			RedFrame,
			TweenInfo.new(1),
			{BackgroundTransparency = 1}
		)
		RedFade:Play()

		FadeTween.Completed:Connect(function()
			Sound:Destroy()
			ScreenGui:Destroy()
		end)
	 end
  end)
end)

